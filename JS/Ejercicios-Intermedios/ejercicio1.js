/*1. Ordenar alfabéticamente
Dado un arreglo de strings, ordena el arreglo en orden alfabético.*/

let arregloAlfabetico
function ordenarAlfabeticamente(arr) {
   arregloAlfabetico =  arr.sort()
   return arregloAlfabetico
}
console.log(ordenarAlfabeticamente(["Mari", "Claudia", "Yenifer", "Alondra"]))

